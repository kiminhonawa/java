package edu.java.loop04;

public class LoopMain04 {

	public static void main(String[] args) {
	//while 반복문
		int n = 1;
		while (n <=10) {
			System.out.print(n + "");
			n++;
		}
		System.out.println();
	
		//do-while 반복문
		int o = 1;
		do {
			System.out.print(o + "");
			o++;
		} while (o <=10);
		
	
	}
}
